create or replace TABLE DIM_CUSTOMER (
	CUSTOMER_SK NUMBER(38,0) autoincrement start 1 increment 1 noorder,
	CUSTOMER_ID NUMBER(38,0),
	CUSTOMER_NAME VARCHAR(16777216),
	EMAIL VARCHAR(16777216) WITH MASKING POLICY ZOMATO_DWH.UTIL.MP_EMAIL_MASK,
	PRIMARY_PHONE VARCHAR(16777216) WITH MASKING POLICY ZOMATO_DWH.UTIL.MP_PHONE_MASK,
	CITY VARCHAR(16777216),
	AREA VARCHAR(16777216),
	SEGMENT VARCHAR(16777216),
	IS_PRIME_MEMBER BOOLEAN,
	STATUS VARCHAR(16777216),
	EFFECTIVE_FROM TIMESTAMP_NTZ(9),
	EFFECTIVE_TO TIMESTAMP_NTZ(9),
	IS_CURRENT BOOLEAN,
	RECORD_SOURCE VARCHAR(16777216)
);
